package solid.srp;

public class BookInfo {
    String title;
    int price;
    String category;

    public BookInfo(String title,int price, String category){
        this.title=title;
        this.price=price;
        this.category=category;
    }
}

class AuthorInfo{
    String author;
    int authorAge;
    String authorAddress;

    public AuthorInfo(String author,int authorAge,String authorAddress){
        this.author=author;
        this.authorAge=authorAge;
        this.authorAddress=authorAddress;
    }
}





